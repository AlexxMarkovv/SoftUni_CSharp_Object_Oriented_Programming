
namespace Operations
{
    public class MathOperations
    {
        public int Add(int x, int y)
        {
            return x + y;
        }

        public double Add(double x, double y, double r)
        {
            return x + y + r;
        }

        public decimal Add(decimal x, decimal y, decimal r)
        {
            return x + y + r;
        }
    }
}
